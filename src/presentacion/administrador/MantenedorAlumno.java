    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
        // TODO add your handling code here:
        //0=si  1=no    2=cancel
        
        if (btnEliminar.isEnabled()) {
            int resp = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro?", "Alerta!", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.ERROR_MESSAGE);
           
            if (resp == 0) {
                
                try {
                    controladorAlumno.destroy(alumno.getId());
                    JOptionPane.showMessageDialog(this, "Eliminacion satisfactoria");
                } catch (IllegalOrphanException | NonexistentEntityException ex) {
                    JOptionPane.showMessageDialog(this, "No se pudo eliminar");
                }
                
                listarTodo();
                
            } else {
                JOptionPane.showMessageDialog(this, "Se cancelo su operacion");
            }
            activarBotones(true, false, false);
        }
        
    }//GEN-LAST:event_btnEliminarMouseClicked

