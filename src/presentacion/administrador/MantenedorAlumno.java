    private void listarTodo() {

        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Nombre Completo");
        modelo.addColumn("DNI");

        listaDeAlumnos = controladorAlumno.findAlumnoEntities();
        for (Alumno alumno : listaDeAlumnos) {
            modelo.addRow(new String[]{
                alumno.getNombre() + ",   "
                + alumno.getApellidoPaterno() + "   "
                + alumno.getApellidoMaterno() + "   ",
                alumno.getDni()
            });
        }
        tblAlumno.setModel(modelo);
    }
    
    private void activarBotones(boolean a, boolean b, boolean c) {
        btnNuevo.setEnabled(a);
        btnModificar.setEnabled(b);
        btnEliminar.setEnabled(c);
    }
    
    
