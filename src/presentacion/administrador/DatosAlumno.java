    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:

        if (txtNombre.getText().equals("") || txtApellidoMaterno.getText().equals("")
            || txtApellidoPaterno.getText().equals("") || txtDNI.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe ingresar los campos requerido con (*) ", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        alumno = llenarObjeto(alumno);
        
        if (alumno.getId() == 0) {
            nuevoAlumno(alumno);
        }else{
            actualizarAlumno(alumno);
        }
        

    }//GEN-LAST:event_btnGuardarActionPerformed

