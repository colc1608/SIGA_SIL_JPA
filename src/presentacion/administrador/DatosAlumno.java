    private void cargarDatos() {
        txtNombre.setText(alumno.getNombre());
        txtApellidoPaterno.setText(alumno.getApellidoPaterno());
        txtApellidoMaterno.setText(alumno.getApellidoMaterno());
        txtDNI.setText(alumno.getDni());
        txtMovil.setText(alumno.getMovil());
        txtTelefono.setText(alumno.getTelefono());
        txtEmail.setText(alumno.getEmail());
        spnFechaNacimiento.setValue(alumno.getFechadenacimiento());
    }
    
    private void nuevoAlumno(Alumno alumno) {
        try{
            controllerAlumno.createAlumno(alumno);
            JOptionPane.showMessageDialog(null, "Se registro correctamente", "Satisfactorio", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Problemas al registrar ", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void actualizarAlumno(Alumno alumno) {
        try{
            controllerAlumno.edit(alumno);
            JOptionPane.showMessageDialog(null, "Se actualizaron los datos", "Satisfactorio", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Problemas al actualizar ", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private Alumno llenarObjeto(Alumno alumno) {
                
        alumno.setNombre(txtNombre.getText());
        alumno.setApellidoPaterno(txtApellidoPaterno.getText());
        alumno.setApellidoMaterno(txtApellidoMaterno.getText());
        alumno.setDni(txtDNI.getText());
        alumno.setMovil(txtMovil.getText());
        alumno.setTelefono(txtTelefono.getText());
        alumno.setEmail(txtEmail.getText());
        alumno.setFechadenacimiento((Date) spnFechaNacimiento.getValue());
        return alumno;
    }
